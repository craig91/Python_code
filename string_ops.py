# String operations in python

C = 'craig' # a variable that is holding a string
len(C) # the len (length) function shows you the length of whats inside of the variable

C[0] # will return the 0th index from the content inside the variable

C[1:3] #python's way of slicing, the 1 is the 1th index you slice and the 3 is the element at index 3 that gets sliced
